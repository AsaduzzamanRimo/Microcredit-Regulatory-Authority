package com.doer.mraims.core.util.model;

public class Address {
	private String careOf;
	private String houseNo;
	private String roadNo;
	private String villageOrWord;
	private String postOffice;
	private String postcode;
	private String thana;
	private String thanaOid;
	private String district;
	private String districtOid;
	
	public String getCareOf() {
		return careOf;
	}
	public void setCareOf(String careOf) {
		this.careOf = careOf;
	}
	public String getHouseNo() {
		return houseNo;
	}
	public void setHouseNo(String houseNo) {
		this.houseNo = houseNo;
	}
	public String getRoadNo() {
		return roadNo;
	}
	public void setRoadNo(String roadNo) {
		this.roadNo = roadNo;
	}
	public String getVillageOrWord() {
		return villageOrWord;
	}
	public void setVillageOrWord(String villageOrWord) {
		this.villageOrWord = villageOrWord;
	}
	public String getPostOffice() {
		return postOffice;
	}
	public void setPostOffice(String postOffice) {
		this.postOffice = postOffice;
	}
	public String getPostcode() {
		return postcode;
	}
	public void setPostcode(String postcode) {
		this.postcode = postcode;
	}
	public String getThana() {
		return thana;
	}
	public void setThana(String thana) {
		this.thana = thana;
	}
	public String getThanaOid() {
		return thanaOid;
	}
	public void setThanaOid(String thanaOid) {
		this.thanaOid = thanaOid;
	}
	public String getDistrict() {
		return district;
	}
	public void setDistrict(String district) {
		this.district = district;
	}
	public String getDistrictOid() {
		return districtOid;
	}
	public void setDistrictOid(String districtOid) {
		this.districtOid = districtOid;
	}	
}
